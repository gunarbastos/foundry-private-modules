/**
 * Extract a human-readable label from an icon class.
 * e.g. "fa-solid fa-dragon" → "Dragon"
 * @param {string} iconClass
 * @returns {string}
 */
export function getIconLabel(iconClass) {
  const parts = iconClass.split(' ');
  const namePart = parts.find(p => p.startsWith('fa-') && p !== 'fa-solid' && p !== 'fa-regular');
  if (!namePart) return iconClass;
  return namePart
    .replace('fa-', '')
    .split('-')
    .map(word => word.charAt(0).toUpperCase() + word.slice(1))
    .join(' ');
}
